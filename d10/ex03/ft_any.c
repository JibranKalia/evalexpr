int		ft_any(char **tab, int (*f)(char*))
{
	int i;
	int res;

	i = 0;
	res = 0;
	while (tab[i] != 0)
	{
		res = f(tab[i]);
		if (res == 1)
			return (1);
		i++;
	}
	return (0);
}
