int		ft_atoi(char *str)
{
	int		answer;

	answer = 0;
	while (*str)
	{
		if (*str && (*str >= '0' && *str <= '9'))
			answer = answer * 10 + (*str - '0');
		else
			return (0);
		str++;
	}
	return (answer);
}
