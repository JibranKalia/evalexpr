int		ft_strcmp(char *s1, char *s2)
{
	int		diff;
	int		i;

	diff = 0;
	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != '\0')
			diff = diff + s1[i];
		if (s2[i] != '\0')
			diff = diff - s2[i];
		if (diff != 0)
			break ;
		i++;
	}
	return (diff);
}
